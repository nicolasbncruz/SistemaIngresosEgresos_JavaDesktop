    private void ListarCliente(String valorBusqueda, int pagina, String cantidad) {
        double totalRegistro = cldao.total(valorBusqueda);
        int desde = (pagina - 1) * Integer.parseInt(cantidad);
        int totalPage = (int) Math.ceil(totalRegistro / Double.parseDouble(cantidad));

        List<ClientesModel> Listar = cldao.listar(valorBusqueda, desde, Integer.parseInt(cantidad));
        modelo = (DefaultTableModel) tblClientes.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < Listar.size(); i++) {
            ob[0] = Listar.get(i).getId();
            ob[1] = Listar.get(i).getNombre();
            ob[2] = Listar.get(i).getApellido();
            ob[3] = Listar.get(i).getCorreo();
            ob[4] = Listar.get(i).getDireccion();
            ob[5] = Listar.get(i).getTelefono();
            modelo.addRow(ob);
        }
        tblClientes.setModel(modelo);
        colorTable(tblClientes);
        pagination(btnAntCli, btnSigCli, pagCliente, pagina, totalPage);

    }

