    private void btnSaveEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveEmpActionPerformed
        if (!"".equals(txtNomEmp.getText()) && !"".equals(txtApeEmp.getText()) && !"".equals(txtCorEmp.getText()) && !"".equals(txtDirEmp.getText()) && !"".equals(txtClaveEmp.getText())) {
            EmpleadosModel emp = new EmpleadosModel();
            emp.setNombre(txtNomEmp.getText());
            emp.setApellido(txtApeEmp.getText());
            emp.setCorreo(txtCorEmp.getText());
            emp.setDireccion(txtDirEmp.getText());
            emp.setClave(txtClaveEmp.getText());
            if (txtIdEmp.getText().length() > 0) {
                emp.setId(Integer.parseInt(txtIdEmp.getText()));
                String respuesta = empdao.modificar(emp);
                switch (respuesta) {
                    case "existe":
                        JOptionPane.showMessageDialog(null, "EMPLEADO YA EXISTE");
                        break;
                    case "modificado":
                        JOptionPane.showMessageDialog(null, "EMPLEADO MODIFICADO");
                        limpiarEmpleado();
                        LimpiarTable();
                        ListarEmpleado(txtBuscarEmpleado.getText(), Integer.parseInt(page.getText()), porPagina);
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "ERROR AL MODIFICAR");
                        break;
                }

            } else {
                String respuesta = empdao.registrar(emp);
                switch (respuesta) {
                    case "existe":
                        JOptionPane.showMessageDialog(null, "EMPLEADO YA EXISTE");
                        break;
                    case "registrado":
                        JOptionPane.showMessageDialog(null, "EMPLEADO REGISTRADO");
                        limpiarEmpleado();
                        LimpiarTable();
                        ListarEmpleado(txtBuscarEmpleado.getText(), Integer.parseInt(page.getText()), porPagina);                    
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "ERROR AL REGISTRAR");
                        break;
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Los campos estan vacios");
        }
    }//GEN-LAST:event_btnSaveEmpActionPerformed

